# Escape sequence are used to include special characters in strings
# Common escape sequence characters with examples:

# \n - New line
print("Hey how are you ?\nI am good")

# \t - Tab/space 
print("My name is \t Mohit Rajpal")

# \\- Backslash, we use double backslash to represent a single backslash in a string, its a python rule
print("What are you are seeing is a double backslash \\ , but it will print a single backslash")

# \" - Double quotes, if you want to have a double quotes or single quotes you have to use backslash for it.
print("To have a double quote in your string, type \"")

# \' - Single quotes
print("To have a single quote, type \'")