# 3 - Write a program that takes a number from the user and prints "Even" if it is even, otherwise "Odd"

num = int(input("Enter a number: "))

if num%2 == 0:
    print("Its an even number")

# If a number leaves 0 as an remainder after being divisible by 2, means it's an even number

else :
    print("Its a odd number!")

# And if the number is not divisible by 2, its an odd number.