# 3 - Logical operators are used to perform operations on booleans
# and, or, not operator

# Examples of logical operators
a = True
b = False

# and operator
print(a and b) 
print(a and a)
print(b and a)
print(b and b)
# and operator always prints true if both of them are true otherwise false.

# or operator
print(a or b) 
print(a or a)
print(b or a)
print(b or b)
# or operator prints true everytime if even one of them is true, kind of opposite to and operator.

# not operator
print(not(True)) 
print(not(False))
# Its going to print the opposite of what the value is given. it helps when you want to negate the value