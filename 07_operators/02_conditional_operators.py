# 2 - Comparision/Conditional operator, it always gives you the output in true or false, similar to boolean.

a = 100

b = 50

# Examples of conditional operator.
print(a > b)
print(a < b)
print(a < 101)
print(a == 101) #Equals to sign, cause a single equal(=) to is an assignment operator it helps to stores the value. 
print(a != 101) #Not equal sign, means a is not equal to 101
print(a >= 101) # Greater or equal sign
print(a <= 101) # Less than or equal sign
print(a == b)
print(a==b, b==a, b==1001,)
print(a==b==1001)
