# 4 - Assignment operators, they are used to perform operations on the assignment or on the equals to sign(=).

# Examples of assignment operators
a = 50
print(a)

b = 100
b +=5     # Equivalent to x = x + 5 , so x = 100, 100 + 5 = 105.
print(b)

c = 101
c -= 50
print(c)

d = 500
d %= 5
print(d)

e = 10
e *= 10
print(e)

f = 300
f /= 10
print(f)

g = 500
g //= 50
print(g)

h = 2
h **= 2
print(h)
