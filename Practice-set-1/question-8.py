# Q8: Operator Challenge

'''
Write a program that:

Takes an integer as input from the user.
Prints the square and cube of that number.
'''

# Solution

a = int(input("Enter your Number: "))

print("a ** 2 = ", a**2)    #Square
print("a ** 3 = ", a**3)    #Cube

# What it does is it takes integer as an input from the user, then it uses exponentian operator(**) to solve it, to get the square and cube of that integer.