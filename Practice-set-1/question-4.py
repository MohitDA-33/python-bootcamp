# Q4: Typecasting Practice

# You are given a string:
'''
num = "45"
'''
'''
Convert it into an integer
Add 10 to it
Print the result
'''

# Solution:
num = int("45")

print("45 + 10 = ",num+10) 

# We used the typecasting to change the data type, then i used the arithmetic operator to add 10 to it, equaling to (=) 55.