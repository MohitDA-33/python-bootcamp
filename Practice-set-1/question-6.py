# Q6: Simple Calculator

'''
Write a program that:

Takes two numbers as input from the user.

Prints their:

Sum         a + b
Difference  a - b
Product     a * b
Quotient    a / b
'''

# Solution
a = int(input("Enter your First number: "))
b = int(input("Enter your Second number: "))

print("a + b = ", a+b)
print("a - b = ", a-b)
print("a * b = ", a*b)
print("a / b = ", a/b)

# What it does is, it asks for numbers from the user as an input and then prints it, after arithemetic operations are perfomed on that numbers.